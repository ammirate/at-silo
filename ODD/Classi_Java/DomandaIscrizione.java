package atsilo;

public class DomandaIscrizione {
	
	private String data;
	private int id;
	private int punteggio;
	private String posizione;
	private String genitore;
	private String bambino;
	
	public DomandaIscrizione(){}

	/**
	 * @return the data
	 */
	public String getData() {
		return data;
	}

	/**
	 * @param data the data to set
	 */
	public void setData(String data) {
		this.data = data;
	}

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the punteggio
	 */
	public int getPunteggio() {
		return punteggio;
	}

	/**
	 * @param punteggio the punteggio to set
	 */
	public void setPunteggio(int punteggio) {
		this.punteggio = punteggio;
	}

	/**
	 * @return the posizione
	 */
	public String getPosizione() {
		return posizione;
	}

	/**
	 * @param posizione the posizione to set
	 */
	public void setPosizione(String posizione) {
		this.posizione = posizione;
	}

	/**
	 * @return the genitore
	 */
	public String getGenitore() {
		return genitore;
	}

	/**
	 * @param genitore the genitore to set
	 */
	public void setGenitore(String genitore) {
		this.genitore = genitore;
	}

	/**
	 * @return the bambino
	 */
	public String getBambino() {
		return bambino;
	}

	/**
	 * @param bambino the bambino to set
	 */
	public void setBambino(String bambino) {
		this.bambino = bambino;
	}
	
	

}
